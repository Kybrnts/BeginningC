/* Program 1.5 Another Simple C Program - Displaying Great Quotations */
#include <stdio.h>

int main(void) {
  printf ("\n\"It is a wise father that knows his own child.\" Shakespeare\n");
  /* * The double quotes are output because you use escape sequences for them in the string.
   * * "Shakespeare" will appear on the next line because there is a \n escape sequence following the \".
   */
  return 0;
}
